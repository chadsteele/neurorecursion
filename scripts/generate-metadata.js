/**
 * Pre-build script: Generates conditions-metadata.js from Conditions.js and Pioneers.js
 * Ensures metadata is always in sync with source data
 *
 * Usage: node scripts/generate-metadata.js
 */

import fs from "fs"
import path from "path"
import {fileURLToPath} from "url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const rootDir = path.join(__dirname, "..")

// Paths to source files
const conditionsPath = path.join(rootDir, "src/data/Conditions.js")
const pioneersPath = path.join(rootDir, "src/data/Pioneers.js")
const outputPath = path.join(rootDir, "src/data/conditions-metadata.js")

/**
 * Extract id, path, and background_image from Conditions.js
 * Uses direct module import for maximum accuracy
 */
async function extractConditions() {
	try {
		const conditionsModule = await import(`file://${conditionsPath}`)
		const conditions = conditionsModule.default || []

		if (!Array.isArray(conditions)) {
			throw new Error("Conditions.js default export is not an array")
		}

		// Extract only id, path, background_image from each condition
		return conditions.map((condition) => ({
			id: condition.id,
			path: condition.path,
			background_image: condition.background_image || "",
		}))
	} catch (error) {
		console.error("Failed to import Conditions.js:", error.message)
		throw error
	}
}

/**
 * Extract id, path, and background_image from Pioneers.js
 * Uses direct module import for maximum accuracy
 */
async function extractPioneers() {
	try {
		const pioneersModule = await import(`file://${pioneersPath}`)
		const pioneers = pioneersModule.default || []

		if (!Array.isArray(pioneers)) {
			throw new Error("Pioneers.js default export is not an array")
		}

		// Extract only id, path, background_url (renamed to background_image) from each pioneer
		return pioneers.map((pioneer) => ({
			id: pioneer.id,
			path: pioneer.path,
			background_image: pioneer.background_url || "",
		}))
	} catch (error) {
		console.error("Failed to import Pioneers.js:", error.message)
		throw error
	}
}

/**
 * Generate the metadata file content
 */
function generateMetadataFile(conditions, pioneers) {
	const header = `// Shared metadata for both Conditions and Pioneers
// Used by both SvelteKit and Netlify Edge Functions to keep paths and images in sync
// This file is auto-generated by scripts/generate-metadata.js

`

	const conditionsExport = `export const CONDITIONS_METADATA = ${JSON.stringify(conditions, null, 1)}\n`

	const pioneersExport = `export const PIONEERS_METADATA = ${JSON.stringify(pioneers, null, 1)}\n`

	return header + conditionsExport + "\n" + pioneersExport
}

/**
 * Main function
 */
async function main() {
	try {
		console.log("📊 Generating metadata...")
		console.log(`  Reading ${path.relative(rootDir, conditionsPath)}`)
		const conditions = await extractConditions()
		console.log(`  ✓ Extracted ${conditions.length} conditions`)

		console.log(`  Reading ${path.relative(rootDir, pioneersPath)}`)
		const pioneers = await extractPioneers()
		console.log(`  ✓ Extracted ${pioneers.length} pioneers`)

		const content = generateMetadataFile(conditions, pioneers)

		console.log(`  Writing ${path.relative(rootDir, outputPath)}`)
		fs.writeFileSync(outputPath, content, "utf-8")
		console.log(
			`✅ Metadata generated successfully (${conditions.length} conditions + ${pioneers.length} pioneers)`,
		)
	} catch (error) {
		console.error("❌ Error generating metadata:")
		console.error(error.message)
		process.exit(1)
	}
}

main()
