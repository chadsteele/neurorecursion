const groups = Object.fromEntries(
	Object.entries(groupMembers).map(([groupName, members]) => [
		groupName,
		new Set(members),
	]),
)

const memberships = new Map(conditionIds.map((id) => [id, []]))

for (const [groupName, members] of Object.entries(groups)) {
	for (const id of members) {
		if (memberships.has(id)) {
			memberships.get(id).push(groupName)
		}
	}
}

function scoreCandidate(sourceId, candidateId) {
	let score = 0
	const sourceGroups = memberships.get(sourceId) || []
	const candidateGroups = memberships.get(candidateId) || []

	for (const groupName of sourceGroups) {
		if (candidateGroups.includes(groupName)) score += 20
		for (const bridgeGroup of bridgePriority[groupName] || []) {
			if (candidateGroups.includes(bridgeGroup)) score += 6
		}
	}

	if (
		candidateId.startsWith("general-") &&
		!sourceId.startsWith("general-")
	) {
		score -= 2
	}

	return score
}

function rankedCandidates(sourceId) {
	if (manualOverrides[sourceId]) {
		return manualOverrides[sourceId]
	}

	const sourceGroups = memberships.get(sourceId) || []
	const picked = []
	const seen = new Set([sourceId])

	const pick = (candidateId) => {
		if (
			!candidateId ||
			seen.has(candidateId) ||
			!conditionIds.includes(candidateId) ||
			picked.length >= 7
		) {
			return
		}

		seen.add(candidateId)
		picked.push(candidateId)
	}

	for (const groupName of sourceGroups) {
		for (const candidateId of preferredWithinGroup[groupName] || []) {
			pick(candidateId)
		}
	}

	const scored = conditionIds
		.filter((id) => id !== sourceId)
		.map((id) => ({id, score: scoreCandidate(sourceId, id)}))
		.filter((entry) => entry.score > 0)
		.sort(
			(left, right) =>
				right.score - left.score || left.id.localeCompare(right.id),
		)

	for (const entry of scored) {
		pick(entry.id)
	}

	if (picked.length === 0) {
		for (const fallbackId of [
			"generalized-anxiety-disorder",
			"general-mood-disorders",
			"self-esteem-identity-issues",
			"general-attachment-issues",
		]) {
			pick(fallbackId)
		}
	}

	return picked.slice(0, 7)
}

const RelatedConditions = Object.fromEntries(
	conditionIds.map((id) => [id, rankedCandidates(id)]),
)

