<h2>Role Description</h2>
<p>
	The Clinical Research Associate will design, conduct, and publish research
	studies related to neural feedback loops and neuroplasticity. You will be
	scheduled to conduct clinical trials via video calls, facilitating the
	direct application of NRI protocols. Responsibilities include data
	collection and analysis, developing methodologies, reviewing academic
	literature, and contributing to the design of clinical protocols.
	Collaboration with multidisciplinary teams and active participation in
	peer-reviewed publication processes are integral to this role.
</p>

